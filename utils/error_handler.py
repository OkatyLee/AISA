    async def handle_summarization_error(callback: CallbackQuery, error: Exception):
        """–û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—à–∏–±–æ–∫ —Å—É–º–º–∞—Ä–∏–∑–∞—Ü–∏–∏"""
