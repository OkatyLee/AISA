    @staticmethod
    def log_unexpected_error(context: str, error: Exception, user_data: Optional[Dict[str, Any]] = None):
        """–õ–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ –Ω–µ–æ–∂–∏–¥–∞–Ω–Ω—ã—Ö –æ—à–∏–±–æ–∫"""
        log_message = f"Unexpected error in {context}: {error}"
